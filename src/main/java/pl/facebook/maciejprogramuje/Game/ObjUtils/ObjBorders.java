package pl.facebook.maciejprogramuje.PanesObjs.ObjUtils;

public class ObjBorders {
    private Double topBorder, bottomBorder, leftBorder, rightBorder;

    public Double getTopBorder() {
        return topBorder;
    }

    public void setTopBorder(Double topBorder) {
        this.topBorder = topBorder;
    }

    public Double getBottomBorder() {
        return bottomBorder;
    }

    public void setBottomBorder(Double bottomBorder) {
        this.bottomBorder = bottomBorder;
    }

    public Double getLeftBorder() {
        return leftBorder;
    }

    public void setLeftBorder(Double leftBorder) {
        this.leftBorder = leftBorder;
    }

    public Double getRightBorder() {
        return rightBorder;
    }

    public void setRightBorder(Double rightBorder) {
        this.rightBorder = rightBorder;
    }
}
